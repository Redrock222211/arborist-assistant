  /// Get LGA tree laws (local council bylaws)
  static Map<String, dynamic>? getLGATreeLaws(String lga) {
    return _lgaTreeLaws[lga];
  }

  /// Get Victorian planning overlays for a specific LGA
  static Map<String, Map<String, dynamic>>? getVictorianPlanningOverlays(String lga) {
    return _victorianPlanningOverlays[lga];
  }

  /// Get all comprehensive data for an LGA (both local laws and planning overlays)
  static Map<String, dynamic> getAllLGAData(String lga) {
    final localLaws = getLGATreeLaws(lga);
    final planningOverlays = getVictorianPlanningOverlays(lga);
    
    return {
      'lga': lga,
      'local_tree_laws': localLaws,
      'victorian_planning_overlays': planningOverlays,
      'has_local_laws': localLaws != null,
      'has_planning_overlays': planningOverlays != null && planningOverlays.isNotEmpty,
      'total_data_sources': (localLaws != null ? 1 : 0) + (planningOverlays != null ? planningOverlays.length : 0),
      'last_updated': '2024-08-01',
    };
  }

  /// Check if an LGA has Victorian planning overlays
  static bool hasVictorianPlanningOverlays(String lga) {
    return _victorianPlanningOverlays.containsKey(lga) && _victorianPlanningOverlays[lga]!.isNotEmpty;
  }

  /// Get all LGAs that have Victorian planning overlays
  static List<String> getLGAsWithVictorianPlanningOverlays() {
    return _victorianPlanningOverlays.keys.toList();
  }

  /// Get comprehensive summary for an LGA
  static Map<String, dynamic> getComprehensiveLGASummary(String lga) {
    final localLaws = getLGATreeLaws(lga);
    final planningOverlays = getVictorianPlanningOverlays(lga);
    
    return {
      'lga': lga,
      'has_local_tree_laws': localLaws != null,
      'has_victorian_planning_overlays': planningOverlays != null && planningOverlays.isNotEmpty,
      'local_law_summary': localLaws != null ? 'Local tree protection bylaws apply' : 'No local tree protection bylaws',
      'planning_overlay_summary': planningOverlays != null && planningOverlays.isNotEmpty 
          ? '${planningOverlays.length} Victorian planning overlay(s) apply' 
          : 'No Victorian planning overlays apply',
      'total_protection_levels': (localLaws != null ? 1 : 0) + (planningOverlays != null ? planningOverlays.length : 0),
      'note': 'Local laws and Victorian overlays are separate protection systems that can both apply',
    };
  }

  /// Get overlay type description
  static String _getOverlayType(String overlayCode) {
    if (overlayCode.startsWith('VPO')) return 'Vegetation Protection';
    if (overlayCode.startsWith('ESO')) return 'Environmental Significance';
    if (overlayCode.startsWith('SLO')) return 'Significant Landscape';
    if (overlayCode.startsWith('HO')) return 'Heritage';
    if (overlayCode.startsWith('DDO')) return 'Design and Development';
    return 'Planning Overlay';
  }

  /// Get permit data for a specific LGA and overlay
  static Map<String, dynamic>? getPermitData(String lga, String overlayCode) {
    return _victorianPlanningOverlays[lga]?[overlayCode];
  }

  /// Get all overlays for a specific LGA
  static List<String> getOverlaysForLGA(String lga) {
    return _victorianPlanningOverlays[lga]?.keys.toList() ?? [];
  }

  /// Check if real data exists for an LGA
  static bool hasRealData(String lga) {
    return hasLocalTreeLaws(lga) || hasVictorianPlanningOverlays(lga);
  }

  /// Get all LGAs
  static List<String> getAllLGAs() {
    final allLGAs = <String>{};
    allLGAs.addAll(_lgaTreeLaws.keys);
    allLGAs.addAll(_victorianPlanningOverlays.keys);
    return allLGAs.toList();
  }

  /// Get all overlay codes
